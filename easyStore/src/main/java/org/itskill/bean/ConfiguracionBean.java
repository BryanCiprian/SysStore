package org.itskill.bean;

public class ConfiguracionBean {
	private String  id_config;
	private String  ruc_empresa;
	private String  nom_empresa;
	private String  dir_empresa;
	private String  url_logo_empresa;
	private String  pos_menu;
	private String  foot_empresa;
	private String  usuario;
	public String getId_config() {
		return id_config;
	}
	public void setId_config(String id_config) {
		this.id_config = id_config;
	}
	public String getRuc_empresa() {
		return ruc_empresa;
	}
	public void setRuc_empresa(String ruc_empresa) {
		this.ruc_empresa = ruc_empresa;
	}
	public String getNom_empresa() {
		return nom_empresa;
	}
	public void setNom_empresa(String nom_empresa) {
		this.nom_empresa = nom_empresa;
	}
	public String getDir_empresa() {
		return dir_empresa;
	}
	public void setDir_empresa(String dir_empresa) {
		this.dir_empresa = dir_empresa;
	}
	public String getUrl_logo_empresa() {
		return url_logo_empresa;
	}
	public void setUrl_logo_empresa(String url_logo_empresa) {
		this.url_logo_empresa = url_logo_empresa;
	}
	public String getPos_menu() {
		return pos_menu;
	}
	public void setPos_menu(String pos_menu) {
		this.pos_menu = pos_menu;
	}
	public String getFoot_empresa() {
		return foot_empresa;
	}
	public void setFoot_empresa(String foot_empresa) {
		this.foot_empresa = foot_empresa;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	
	
	
	 
}
